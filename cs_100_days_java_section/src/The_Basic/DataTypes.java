package The_Basic;

public class DataTypes {
    public static void main(String[] args) {

        //Primitive DataTypes

        byte num1 = 125;

        short num2 = 12345;

        int num3 = 123456; // 32 bit

        long num4 = 12345678L; //64 bit

        float floatNumber = 3.1415926f; // 32 bit

        double doubleNumber = 1.234567890; // 64 bit

        System.out.println(floatNumber);
        System.out.println(doubleNumber);

        boolean booValue1 = true;
        boolean booValue2 = false;

        System.out.println(booValue1);
        System.out.println(booValue2);

        char character = 'M'; // 77 = M
        char character1 = 77;

        System.out.println(character);

        
    }
}
